/**
 * ConsoleColors defines color codes for terminal output and tags.
 */
export enum ConsoleColors {
  DEFAULT = '#ffffff', // Default terminal color
  GREEN = '#00ff00', // Success/info
  YELLOW = '#ffff00', // Warnings
  TAG_INFO = '#87ceeb', // Info tag
  TAG_SUCCESS = '#98fb98', // Success tag
  TAG_ERROR = '#ff6b6b', // Error tag
  TAG_WARN = '#ffd700', // Warning tag
  TAG_ENGINE = '#ffa500', // Engine tag
  TAG_MQTT = '#9370db', // MQTT tag
  TAG_SIMULATION = '#20b2aa', // Simulation tag
}
